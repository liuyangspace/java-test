package javase.data;

/**
 * The {@code Void} class is an uninstantiable placeholder class to hold a
 * reference to the {@code Class} object representing the Java keyword
 * void.
 *
 * @see java.lang.Void
 */
public final class Void {
    /*
     * The Void class cannot be instantiated.
     */
    private Void() {}
}
